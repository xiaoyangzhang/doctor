package com.yhyt.health.model.vo;

import java.io.Serializable;
import java.util.Date;

/**
 * Created by shensh on 2017/9/5.
 */
public class DialogRecordVo implements Serializable {
    private String main_diagnose;
    private String sub_diagnose;
    private Date create_time;
    private String title;
    private String realname;
    private String hospitalname;
    private String deptname;

    public String getMain_diagnose() {
        return main_diagnose;
    }

    public void setMain_diagnose(String main_diagnose) {
        this.main_diagnose = main_diagnose;
    }

    public String getSub_diagnose() {
        return sub_diagnose;
    }

    public void setSub_diagnose(String sub_diagnose) {
        this.sub_diagnose = sub_diagnose;
    }

    public Date getCreate_time() {
        return create_time;
    }

    public void setCreate_time(Date create_time) {
        this.create_time = create_time;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getRealname() {
        return realname;
    }

    public void setRealname(String realname) {
        this.realname = realname;
    }

    public String getHospitalname() {
        return hospitalname;
    }

    public void setHospitalname(String hospitalname) {
        this.hospitalname = hospitalname;
    }

    public String getDeptname() {
        return deptname;
    }

    public void setDeptname(String deptname) {
        this.deptname = deptname;
    }
}
