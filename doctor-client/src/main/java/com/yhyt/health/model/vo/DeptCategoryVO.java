package com.yhyt.health.model.vo;

import java.io.Serializable;

public class DeptCategoryVO implements Serializable {

    private static final long serialVersionUID = 1599851683440657374L;
    private String categories;//科室分类字符串
    private Integer count;//科室分类对应的疾病总数
    private Long diseaseId;

    public Long getDiseaseId() {
        return diseaseId;
    }

    public void setDiseaseId(Long diseaseId) {
        this.diseaseId = diseaseId;
    }

    public String getCategories() {
        return categories;
    }

    public void setCategories(String categories) {
        this.categories = categories;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }
}
