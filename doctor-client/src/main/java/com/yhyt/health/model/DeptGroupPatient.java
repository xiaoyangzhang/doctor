package com.yhyt.health.model;

import java.util.Date;

public class DeptGroupPatient {
    private Long id;

    private Long deptGroupId;

    private Long patientId;

    private Byte flag;

    private String remark;

    private Byte isBlacklist;

    private Date createTime;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getDeptGroupId() {
        return deptGroupId;
    }

    public void setDeptGroupId(Long deptGroupId) {
        this.deptGroupId = deptGroupId;
    }

    public Long getPatientId() {
        return patientId;
    }

    public void setPatientId(Long patientId) {
        this.patientId = patientId;
    }

    public Byte getFlag() {
        return flag;
    }

    public void setFlag(Byte flag) {
        this.flag = flag;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark == null ? null : remark.trim();
    }

    public Byte getIsBlacklist() {
        return isBlacklist;
    }

    public void setIsBlacklist(Byte isBlacklist) {
        this.isBlacklist = isBlacklist;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
}