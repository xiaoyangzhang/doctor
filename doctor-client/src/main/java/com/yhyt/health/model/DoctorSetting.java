package com.yhyt.health.model;

import java.util.Date;

public class DoctorSetting extends Persistable {

    private Long doctorId;

    private Long departmentId;
    //求诊信息免打扰 1-关闭 2-打开
    private Byte askforDiagnoseOption = 1;
    //诊前信息免打扰 1-关闭 2-打开
    private Byte preDiagnoseOption = 1;
    //诊后信息免打扰 1-关闭 2-打开
    private Byte afterDiagnoseOption = 1;
    //夜间信息免打扰 1-关闭 2-打开
    private Byte nightDiagnoseOption = 1;

    private Date createTime;

    private Date updateTime;

    private Integer type;

    public Long getDoctorId() {
        return doctorId;
    }

    public void setDoctorId(Long doctorId) {
        this.doctorId = doctorId;
    }

    public Long getDepartmentId() {
        return departmentId;
    }

    public void setDepartmentId(Long departmentId) {
        this.departmentId = departmentId;
    }

    public Byte getAskforDiagnoseOption() {
        return askforDiagnoseOption;
    }

    public void setAskforDiagnoseOption(Byte askforDiagnoseOption) {
        this.askforDiagnoseOption = askforDiagnoseOption;
    }

    public Byte getPreDiagnoseOption() {
        return preDiagnoseOption;
    }

    public void setPreDiagnoseOption(Byte preDiagnoseOption) {
        this.preDiagnoseOption = preDiagnoseOption;
    }

    public Byte getAfterDiagnoseOption() {
        return afterDiagnoseOption;
    }

    public void setAfterDiagnoseOption(Byte afterDiagnoseOption) {
        this.afterDiagnoseOption = afterDiagnoseOption;
    }

    public Byte getNightDiagnoseOption() {
        return nightDiagnoseOption;
    }

    public void setNightDiagnoseOption(Byte nightDiagnoseOption) {
        this.nightDiagnoseOption = nightDiagnoseOption;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }
}